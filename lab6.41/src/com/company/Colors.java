public class Colors {
    int Colors;

    void colorMotorcycle(String Color) {

        if (Color == "Red") {
            System.out.println("Червоного кольору");

        } else if (Color == "Blue") {
            System.out.println("Синього кольору");

        } else if (Color == "Blak") {
            System.out.println("Чорного кольору");

        } else if (Color == "Yellow") {
            System.out.println("Жовтого кольору");

        } else if (Color == "Green") {
            System.out.println("Зеленого кольору");

        }else {
            System.out.println("Немає такого кольору");
        }

    }

}


